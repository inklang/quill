#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum TokenType {
    // Literals
    KwBool,
    KwInt,
    KwFloat,
    KwDouble,
    KwString,
    KwTrue,
    KwFalse,
    KwNull,
    // Keywords
    KwLet,
    KwConst,
    KwIf,
    KwElse,
    KwWhile,
    KwFor,
    KwIn,
    KwFn,
    KwReturn,
    KwAnd,
    KwOr,
    KwNot,
    KwBreak,
    KwNext,
    KwEnum,
    KwClass,
    KwExtends,
    KwImport,
    KwFrom,
    KwIs,
    KwHas,
    KwTable,
    KwKey,
    KwConfig,
    KwTry,
    KwCatch,
    KwFinally,
    KwThrow,
    KwOn,
    KwEnable,
    KwDisable,
    KwEvent,
    KwAsync,
    KwAwait,
    KwSpawn,
    KwVirtual,
    // Operators
    Identifier,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Increment,
    Decrement,
    Pow,
    EqEq,
    BangEq,
    Lt,
    Gt,
    Lte,
    Gte,
    Assign,
    Arrow,
    AddEquals,
    SubEquals,
    MulEquals,
    DivEquals,
    ModEquals,
    LBrace,
    RBrace,
    LParen,
    RParen,
    LSquare,
    RSquare,
    Bang,
    Comma,
    Dot,
    DotDot,
    Colon,
    Semicolon,
    Question,
    QuestionDot,
    QuestionQuestion,
    InterpolationStart,
    InterpolationEnd,
    Dollar,
    At,
    KwAnnotation,
    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub typ: TokenType,
    pub lexeme: String,
    pub line: usize,
    pub column: usize,
}
